// var the way to define variable
// if you want changes any varable value, you can
var name = "Sangwoo";
// var allows you redefine the variable
var name = "Lee";

// const is constant
// you can set a constant varible with const
// constant variables cannot be redeclared and changed after first definition
// I strongly encourage to use const to define variables
const car_name = "Sonata";

// let can be changed value, but not allow redefine
let pet_name = "Puppy";

console.log(name);
console.log(car_name);
console.log(pet_name);
